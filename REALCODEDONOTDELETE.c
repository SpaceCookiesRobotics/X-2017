void forward (int time){
	motor[CHFrontRight] = 256;
	motor[CHFrontLeft] = 256;
	motor[CHBackRight] = 256;
	motor[CHBackLeft] = 256;
	wait1Msec(time);
}

void backward (int time){
	motor[CHFrontRight] = -256;
	motor[CHFrontLeft] = -256;
	motor[CHBackRight] = -256;
	motor[CHBackLeft] = -256;
	wait1Msec(time);
}

